function SetCategories() {

for (let i = 0; i < categories.length; i++) {
    const categoryDiv = document.createElement("div");
    categoryDiv.className = "category";
    let catiegoryName = document.createElement("a");
    let catiegoryTarget = document.createElement("a");
    catiegoryName.innerText = categories[i].name;
    catiegoryTarget.innerText = categories[i].title;
    catiegoryTarget.className = "category-title";
    // catiegoryTarget.href = "#";
    categoryDiv.appendChild(catiegoryName);
    categoryDiv.appendChild(catiegoryTarget);

    container.appendChild(categoryDiv);
    if ( !categories[i].subcategories ) continue;
    for (let j = 0; j < categories[i].subcategories.length; j++) {
        const subDiv = document.createElement("div");
        subDiv.className = "subcategory";
        let subName = document.createElement("a");
        let subTarget = document.createElement("a");
        subTarget.className = "subcategory-title";
        subName.innerText = categories[i].subcategories[j].name;
        subTarget.href = "#";
        subTarget.innerText = categories[i].subcategories[j].title;
        subDiv.appendChild(subName);
        subDiv.appendChild(subTarget);
        categoryDiv.appendChild(subDiv);
    }
}
}
